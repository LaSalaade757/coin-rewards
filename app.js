// ---------- Eléments HTML ----------

const cards = document.getElementById("cards");
const searchInput = document.getElementById("searchInput");
const pageTitle = document.getElementById("pageTitle");

const navButtons =
document.querySelectorAll(".bottomBar button");

const filterButtons =
document.querySelectorAll(".filter");

// ---------- Variables ----------

let currentPage = "home";

let currentFilter = "all";

// Récompenses déjà récupérées
let claimedRewards = JSON.parse(
    localStorage.getItem("claimedRewards")
) || [];

// ---------- Fonctions utilitaires ----------

function claimReward(url, status){

    if(status === "expired"){

        alert("Cette récompense est expirée.");

        return;

    }

    // Sauvegarde la récompense récupérée
    if(!claimedRewards.includes(url)){

        claimedRewards.push(url);

        localStorage.setItem(
            "claimedRewards",
            JSON.stringify(claimedRewards)
        );

    }

    // Ouvre le lien
    window.open(url, "_blank");

    // Rafraîchit l'affichage
    renderRewards();

}

function getTimeAgo(reward){

    const now = new Date();

    const [day, month, year] = reward.date.split("/");
    const [hour, minute] = reward.time.split(":");

    const published = new Date(
        year,
        month - 1,
        day,
        hour,
        minute
    );

    const diff = Math.floor((now - published) / 1000);

    if(diff < 60){

        return "À l'instant";

    }

    if(diff < 3600){

        const minutes = Math.floor(diff / 60);

        return `Il y a ${minutes} min`;

    }

    if(diff < 86400){

        const hours = Math.floor(diff / 3600);

        return `Il y a ${hours} heure${hours > 1 ? "s" : ""}`;

    }

    const days = Math.floor(diff / 86400);

    if(days === 1){

        return "Hier";

    }

    return `Il y a ${days} jours`;

}

function getFilteredRewards(){

    const search =
    searchInput.value.toLowerCase();

    return rewards.filter(reward=>{

        const matchSearch =
        reward.title.toLowerCase().includes(search);

        const matchFilter =
        currentFilter === "all"
        ||
        reward.type === currentFilter;

        const matchPage =
        currentPage === "history"
        ||
        reward.status === "available";

        return (
            matchSearch &&
            matchFilter &&
            matchPage
        );

    });

}

function renderRewards(){

    cards.innerHTML="";
    pageTitle.innerHTML="";

    // ---------------- STATS ----------------

    if(currentPage==="stats"){

        const total=rewards.length;

        const available=
        rewards.filter(r=>r.status==="available").length;

        const expired=
        rewards.filter(r=>r.status==="expired").length;

        const spins=
        rewards.filter(r=>r.type==="spin").length;

        const coins=
        rewards.filter(r=>r.type==="coins").length;

        cards.innerHTML=`

<div class="card">

<h2>📊 Statistiques</h2>

<p>📦 Total : <b>${total}</b></p>

<p>🎰 Tours : <b>${spins}</b></p>

<p>🪙 Pièces : <b>${coins}</b></p>

<p>🟢 Disponibles : <b>${available}</b></p>

<p>🔴 Expirées : <b>${expired}</b></p>

</div>

`;

        return;

    }

    // ---------------- PLUS ----------------

    if(currentPage==="more"){

        cards.innerHTML=`

<div class="card">

<h2>⚙️ Plus</h2>

<p><b>Coin Rewards</b></p>

<br>

<p>Version : 1.0</p>

<br>

<p>
Ce site n'est pas affilié à Moon Active.
Les récompenses proviennent des liens officiels publiés par Coin Master.
</p>

</div>

`;

        return;

    }

    // ---------------- HOME / HISTORIQUE ----------------

    const filteredRewards=getFilteredRewards();

    if(filteredRewards.length===0){

        cards.innerHTML=`

<div class="card">

<h2>😢 Aucune récompense</h2>

<p>Aucun résultat trouvé.</p>

</div>

`;

        return;

    }

// Regrouper les récompenses par date
const groupedRewards = {};

filteredRewards.forEach(reward => {

    if(!groupedRewards[reward.date]){

        groupedRewards[reward.date] = [];

    }

    groupedRewards[reward.date].push(reward);

});

// Affichage des groupes
Object.keys(groupedRewards).forEach(date => {

    const rewardsOfDay = groupedRewards[date];

cards.innerHTML += `

<div class="dateSeparator">

    <div class="left">

        <span class="arrow">▼</span>

        <span class="date">📅 ${date}</span>

    </div>

    <span class="count">

        ${rewardsOfDay.length}

    </span>

</div>

`;

    rewardsOfDay.forEach(reward => {

        cards.innerHTML += renderRewardCard(reward);

    });

});

}
